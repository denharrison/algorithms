// Граф представлен как объект, где ключи — люди, а значения — их друзья
const graph = {
    'вы': ['Алиса', 'Боб', 'Клэр'],
    'Алиса': ['Пегги'],
    'Боб': ['Анудж', 'Пегги'],
    'Клэр': ['Том', 'Джонни'],
    'Пегги': [],
    'Анудж': [],
    'Том': [],
    'Джонни': []
  };
  
  // Функция для проверки, является ли человек продавцом манго
  function personIsSeller(person) {
    // В этом примере продавец манго — это человек, чьё имя заканчивается на 'м'
    return person.endsWith('м');
  }
  
  // Функция поиска в ширину (BFS)
  function search(name) {
    // Очередь для хранения людей, которых нужно проверить
    const searchQueue = [...graph[name]];
    // Массив для отслеживания уже проверенных людей
    const searched = [];
  
    // Пока очередь не пуста
    while (searchQueue.length > 0) {
      // Извлекаем первого человека из очереди
      const person = searchQueue.shift();
      console.log(`Проверяем: ${person}`);
  
      // Если человек ещё не проверялся
      if (!searched.includes(person)) {
        // Если это продавец манго
        if (personIsSeller(person)) {
          console.log(`${person} — продавец манго!`);
          return true;
        } else {
          // Добавляем всех друзей этого человека в очередь
          searchQueue.push(...graph[person]);
          // Помечаем человека как проверенного
          searched.push(person);
        }
      }
    }
  
    // Если продавец манго не найден
    console.log('Продавец манго не найден.');
    return false;
  }
  
  // Пример использования
  search('вы');